/**
 * Write cache header hints for hosts that honor `_headers` (Cloudflare Pages, Netlify).
 * GitHub Pages ignores this file and always serves max-age=600.
 */
import { writeFileSync } from "node:fs";
import { join } from "node:path";

const OUT = join(process.cwd(), "out");

const ONE_YEAR = "public, max-age=31536000, immutable";
const HTML = "public, max-age=3600, stale-while-revalidate=86400";

const body = `# Generated by scripts/write-cache-headers.mjs — do not edit in out/
/_next/static/*
  Cache-Control: ${ONE_YEAR}

/img/*
  Cache-Control: ${ONE_YEAR}

/mockify/*
  Cache-Control: ${ONE_YEAR}

/brand/*
  Cache-Control: ${ONE_YEAR}

/*.html
  Cache-Control: ${HTML}

/
  Cache-Control: ${HTML}
`;

writeFileSync(join(OUT, "_headers"), body, "utf8");
console.log("write-cache-headers: wrote out/_headers");
